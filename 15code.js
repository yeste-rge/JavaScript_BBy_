//4.3_i+ vs. ++i
let i = 0;
console.log(i++); //0
//（后置递增）(Post-increment)
console.log(i); //1
console.log("---------"); //---------
i = 0;
console.log(++i); //1
//（前置递增）(Pre-increment)
console.log(i); //1

//extra 4.3_i-- vs. --i
let x = 1;
console.log(i--); //1
console.log(i); //0
let g = 1;
console.log(--g); //0
console.log(g); //0
